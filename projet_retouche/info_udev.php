        <p><a href="https://www.cgi.fr/">CGI</a> crée U'DEV, l'Ecole du Développeur qui accueillera dès le mois de septembre 180 bacheliers sur 6 campus en France (Bordeaux, Clermont-Ferrand, Lille, Lyon, Paris, Toulouse).
