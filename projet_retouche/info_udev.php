      <div class="row infocgi">
        <h1 class="text-center">LE MÉTIER</h1>

            <p>    Le <a href="http://www.onisep.fr/Ressources/Univers-Formation/Formations/Post-bac/Concepteur-developpeur-informatique">Concepteur Développeur Informatique</a> participe, sous la direction d’un chef de projet, à la réalisation d’applications informatiques.
              En fonction de l’analyse des besoins des utilisateurs et de l’étude fonctionnelle, le concepteur développeur informatique va participer à la réalisation de la phase de spécification détaillée, et implémenter la solution avec les technologies retenues, tout en respectant délais et normes de qualité définis (Phases de conception, de développement/programmation, de tests et recettes, de rédaction de documents techniques, de maintenance corrective et évolutive).
