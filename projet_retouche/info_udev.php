          CGI a noué une coopération avec deux écoles reconnues en matière de formation au numérique : <a href="http://www.ipi-ecoles.com/">IPI</a>, l'Ecole de référence spécialiste des parcours individualisés et certificateur de compétences et <a href="http://www.epsi.fr/">EPSI</a>, réseau national,
